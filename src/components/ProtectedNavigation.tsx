"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";
import Image from "next/image";

interface NavUser {
  name?: string | null;
  image?: string | null;
}

const navItems = [
  { href: "/dashboard", label: "Dashboard", icon: DashboardIcon },
  { href: "/my-cars", label: "My Cars", icon: CarIcon },
  { href: "/showcase", label: "Showcase", icon: ShowcaseIcon },
  { href: "/calculator", label: "Calculator", icon: CalculatorIcon },
  { href: "/newsfeed", label: "Newsfeed", icon: NewsfeedIcon },
  { href: "/profile", label: "Profile", icon: ProfileIcon },
];

export function ProtectedNavigation({ user }: { user: NavUser }) {
  const pathname = usePathname();

  return (
    <>
      {/* Desktop sidebar */}
      <aside className="hidden md:flex md:w-64 md:flex-col border-r border-zinc-800 bg-zinc-900">
        <div className="flex items-center gap-3 p-4 border-b border-zinc-800">
          {user.image ? (
            <Image
              src={user.image}
              alt={user.name ?? "User"}
              width={40}
              height={40}
              className="rounded-full"
            />
          ) : (
            <div className="w-10 h-10 rounded-full bg-zinc-700 flex items-center justify-center text-white font-medium">
              {user.name?.[0] ?? "U"}
            </div>
          )}
          <span className="text-sm font-medium text-white truncate">
            {user.name ?? "Member"}
          </span>
        </div>
        <nav className="flex-1 p-3 space-y-1">
          {navItems.map((item) => {
            const isActive = pathname.startsWith(item.href);
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                  isActive
                    ? "bg-zinc-800 text-white"
                    : "text-zinc-400 hover:text-white hover:bg-zinc-800/50"
                }`}
              >
                <item.icon className="w-5 h-5" />
                {item.label}
              </Link>
            );
          })}
        </nav>
      </aside>

      {/* Mobile bottom tab bar */}
      <nav className="fixed bottom-0 left-0 right-0 z-40 flex md:hidden border-t border-zinc-800 bg-zinc-900/95 backdrop-blur-sm">
        {navItems.map((item) => {
          const isActive = pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex flex-1 flex-col items-center gap-1 py-2 text-xs font-medium transition-colors ${
                isActive ? "text-white" : "text-zinc-500"
              }`}
            >
              <item.icon className="w-5 h-5" />
              <span className="truncate">{item.label}</span>
            </Link>
          );
        })}
      </nav>
    </>
  );
}

